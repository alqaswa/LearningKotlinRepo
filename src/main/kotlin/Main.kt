fun main(args: Array<String>)
{
    println("Hello World!")

    //Mutable Variables or variables which can be reassigned
    var age=20
    println("Age is $age")

    age=35
    println("Age is $age")

    var age2:Int=37
    println("Age two is $age2")

    //Immutable variables or variables which cannot be reassigned
    val name="Arshad"

    /*Cannot reassign*/
    //name="Jasmin"
    println("Name is $name")
}